package ru.rkapp;

/**
 * Функциональный интерфейс для вычисления правых частей системы дифференциальных уравнений.
 * Используется в методах Рунге-Кутты для расчета производных.
 */

@FunctionalInterface
public interface RightCalculator {
    /**
     * Вычисляет правые части системы дифференциальных уравнений.
     *
     * @param t    текущее время
     * @param y    массив текущих значений переменных состояния
     * @param f    массив для записи вычисленных правых частей (производных)
     * @param parm пользовательские параметры для вычислений
     * @return true если вычисления успешны, false при ошибке
     */
    boolean compute(double t, double[] y, double[] f, Object parm);
}